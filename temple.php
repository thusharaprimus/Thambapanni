            <div class="details">The worship of corporeal remains of the Buddha, as recorded in the Mahaparinibbana-sutta ( the Record of the Demise of the Buddha), was sanctioned by the Buddha himself on the verge of his passing away. The Buddha declared that four noble persons are worthy of their bodily remains being enshrined and honoured, the Buddha, the Personal Buddhas (Pacceka Buddhas,) the Arahanths (Buddha's disciples) and the Universal Monarchs ( Cakkavatti kings). The bodily remains of the Buddha, after their distribution among various states that claimed for the relics, were enshrined in the funerary mounds known as stupa. However, the four canine Teeth were said to have been separately enshrined and worshipped . The right canine was worshipped in the heavenly domain of the king of gods, Sakra, while another was worshipped by the king of Gandhara in modern Pakistan. The third was taken away by the Nagas and worshipped placing it in a golden shrine room. The fourth, the left canine was removed from the funerary ashes by a monk and was handed over to the king of Kalinga in Eastern India, as recorded in the Digha Nikaya.
            Thenceforth, the Tooth relic of the Kalinga became an object of great veneration by generations of Kalinga kings until it earned the wrath of brahmanical followers, and consequently several attempts were made to destroy the Relic by the fanatical rulers. Yet, the Tooth relic was miraculously saved from such atrocities. For this reason, the kings of other states attempted to possess the Tooth relic for personal veneration. Thus, from the beginning itself, the Tooth relic came to be considered as an important symbol of veneration. The last Indian ruler to possess the Tooth relic was Guhasiva of Kalinga (c.4th century AD).
            The final attempt made by a neighbouring state to make war with Guhasiva for the possession of the Tooth relic caused this venerated relic to leave the Indian shores. By this time, Buddhism was well rooted in Sri Lanka, and the island rulers maintained close relations with the Indian states that fostered Buddhism. Apparently, it was for this reason that the Kalinga ruler, under imminent danger of his loosing in battle, decided to send the Tooth relic to his friend, the Sri Lankan king.
            Next ruler was Parakramabahu IV, during whose time, there was a religious revival. He reorganized the rituals connected with the sacred Tooth Relic in a systematic manner as recorded in the text Dalada Sirita. Yet another significant factor was the handing of the responsibility of the security and conduction of rituals in charge of the chief prelate of the Uttaramula monastery, which institution originated from the Abhayagiri Vihara of Anuradhapura.

            The next ruler of note connected with the story of the Tooth Relic was Bhuvanekabahu IV, who selected a new capital, Gampola, in the central hills. Yet ,no mention is made of his bringing the Tooth Relic to this new city. It was possibly Vikramabahu III who had the Relic shifted to this hill capital and held a festival in honor of the sacred Tooth Relic. He is credited with the building of the shrine at Niyangampaya at Gampola, which comes closer to the 14th century Gadaladeniya temple in the decorative elements.


            Rajasimha II (1634-1686) succeeded king Senarat. As the Portuguese interference in local political affairs was intensified, Rajasimha sought the aid of the Dutch to circumvent the situation. This action did not meet the approval of the people and a chaotic situation arose again, even to the extent of ceasing the holding of the annual Tooth Relic festival and the king leaving the palace. In spite of these troublesome days, he was able to rebuild the sacred Tooth relic shrine into a two-storied structure and place the Tooth Relic therein.

            After Rajasimha II, Vimaladharmasuriya ll (1686-1706) occupied the throne. He was a peace loving ruler and was able to maintain harmonious relations with the Dutch and attend to many religious activities. The higher ordination of monks was re-established with the help of Burmese monks. As the chronicle relates, he built a new three-storied Relic House for the sacred Tooth Relic. This probably constitutes the structure shown in front in the Dutch Plan of 1765. The king is also credited with the preparation of a grand golden casket for the sacred Tooth Relic and he held great festivities and rituals in honor of the sacred Tooth of the Buddha.

            Next in line of the ruling house was king Viraparakrama Narendrasimha (1707-1739). He was the last Sinhalese king of a long line of rulers and is credited with the protection of the sacred Tooth Relic and ritual honours accorded to the Relic. As the Mahavamsa records,he rebuilt the Tooth Relic shrine making it two-storied. The chronicle provides a graphic description of the decoration of the shrine with murals of jatakas, etc. At the recent bomb blast of the Tamil Tigers, amidst destruction and devastation of the Dalada Maligava premises, the structure of this shrine known as Vadahitina Maligava was miraculously saved. The falling off of the top plaster of some parts of the walls of the inner shrine has revealed some of the original murals that would belong to the re-builder of the shrine, Narendrasimha.

            The next ruler was Sri Vijaya Rajasimha (1738-1746) of South Indian Nayakkar origin, who became converted to Buddhism under the influence of Venerable Valivita Saranankara . He was recorded in the Chronicle,Mahavamsa, as having opened the relic casket and seen the sacred Tooth Relic with his own eyes. With this incident, the king went into raptures and held a ceremony for the sacred Tooth Relic, surpassing all other festivities held in the past.

            The second Nayakkar ruler was king Kirti Sri Rajasimha (1746-1779) . He was able to get down monks from Siam (Thailand) under the leadership of Venerable Upali, to hold the Higher Ordination ceremony of the novices including Venerable Welivita Saranankara, who later was declared the Chief Sanghanayaka (Sangharaja) of the Malvatta Chapter. King Kirti Sri remains the greatest benefactor of the sacred Tooth Relic. Many festivities and rituals were instituted by him, including the addition of the Dalada Procession to the Asala festival, which was at the time confined only to the four Devales of Natha, Visnu, Kataragama and Pattini.

            The external interference in local politics resulted in the invasion of Kandy by the Dutch and the Sinhalese seeking the aid of the British, who had already engaged themselves in the maritime trade. For about an year, the Tooth Relic was hidden away in the hills in safer sites. The king too fled to Hanguranketa.

            Finally, the Tooth Relic was brought back to Kandy and placed in the shrine with much veneration and ritual worship was commenced. Thereafter, the third Nayakkar , Rajadhi Rajasimha (1779-1787) became king. Being a devoted Buddhist, he too continued the ritual worship connected with the sacred Tooth Relic .

            It was at this time the two European powers, the Dutch and the British, were enhancing their struggle for supremacy over the island. At this moment, the last king of the Sri Lankan ruling house, Sri Vikrama Rajasimha, (1797-1815) also a South Indian Nayakkar, occupied the throne. His short period ended with the occupation of the island by the British in 1815. Nevertheless, the king was able to continue the rituals connected with the sacred Tooth Relic. Possessing an aesthetic outlook, Sri Vikrama transformed the existing paddy field into a beautiful lake and named it `Kiri Muhuda' (Milky Ocean), thus bringing resplendence to the city of Kandy (ancient Senkadagala).

            Under the prevailing calamitous situations, the Buddhist monks were able to take away the Tooth Relic to many safer locations. The British Governor at the time was Robert Brownrig, who showed some keenness to safeguard the religious activities of the Buddhists, particularly those connected with the sacred Tooth Relic. By now, the Britishers had realized the importance of the Tooth Relic to the natives, and therefore, did their best to please the Buddhists by showing much munificence to the worship of the sacred Tooth Relic. They too seemed to have realized the value of the Tooth Relic as the palladium of kingship. John D'Oyly , who was in charge of Kandy at the time, was originally averse to the Buddhists under the Governor Maitland, but subsequently under the influence of Governor Brownrig, played a friendly role towards the Buddhist religion. Elaborate preparations in the form of festive celebrations and even processions were performed with a view to receiving the sacred Tooth Relic back in the Dalada shrine in Kandy. Yet, doubting the intension of the British, the local prelates and chieftains showed some reluctance to oblige to their request.

            During this period, the Tooth Relic had been moved to several locations such as Bamabaragala, Talagune and Madivaka viharas and the jungles of Bintanna, the country of the Vaddahs, and after the cessation of hostilities to some extent, the Tooth Relic was brought to a location closer to Kandy. Finally, with the help of the Chief Adigar, Ahalepola and chief prelates of both Malvatta and Asgiriya Chapters, the Tooth Relic was taken from its final resting place at Hindagala to Kandy with ceremonial processions arranged with the collaboration of British regiments who themselves marched in the Dalada procession.

            The Britishers faced yet another uprising of the Sinhalese led by Kapptipola in 1818 in the Uva Province. The Kandyans were again plunged into a bloody war situation. In these circumstances, the sacred Tooth Relic was taken away secretly to Hanguranketha by the monk engaged in the daily service (Tevava) of the Relic.The uprising was finally quelled and the sacred Tooth Relic was reposited in the present Dalada shrine and all religious services reinstated, including the holding of daily service and the annual procession.

            The British Government, having realized the importance of the Tooth Relic to be left in charge of the Sinhalese Buddhists, made all necessary arrangements to form a strong committee to overlook the affairs of the Tooth Relic. This committee comprised

            (1).Diva Nilame (Diyawadana Nilame),
            (2). Six important prelates from the Malwatta and the Asgiriya monasteries (3 from each temple), 
            (3). The Chieftains of Udunuwara, Yatinuwara, Dumbara and Harispattuva, and 
            (4). The Basnayakes (Custodians) of the four Devales (Natha, Vishnu, Kataragama and Pattini) and the Maha Saman Devale.

            The Committee was to be chaired by the Diyawadana Nilame ( Lay Custodian of the sacred Tooth Relic). Although the responsibility of the Tooth Relic was handed over to this Committee on 2nd October 1847, yet, the three important personages who acted as the custodians constituted the Maha Nayaka Theros of the Malwatta and Asgiriya Chapters and the Diyawadana Nilame. After the 1848 rebellion, the British Governor took over the responsibilities in regard to the Tooth Relic, and finally in 1853, all responsibilities were vested in the three members mentioned above. However, the British Government seemed to have continued some authority over the Tooth Relic in that, after the completion of the annual Dalada procession, the Diyawadana Nilame and the Basnayake Nilames of the four Devales had to report the successful completion of the event to the Governor at his Lodge. The tradition continues to this day, where at present the Head of the State, the Executive President receives the Nilames at the Kandy official lodge.
